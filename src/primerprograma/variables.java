package primerprograma;
public class variables {
    public static void main(String[] args) {
        char variable_caracter;
        variable_caracter = 'c';
        char segunda_variable_caracter = 126;  //la definimos con el valor del carácter según ASCII el 126 es la virgulilla
        
        System.out.println(variable_caracter);
        System.out.println(segunda_variable_caracter);
        
        int n;                   //Defino una variable entera como n
        n = 10;             //Le doy el valor a mi variable entera
        System.out.println(n);
        
        double decimal = 3.33 ;
        System.out.println(decimal);
        String nombre = "Dalia"; //Defino la variable string como nombre y le doy valor
        System.out.println(nombre);
        
    }
    
}
